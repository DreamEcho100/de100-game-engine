void draw_char(Backbuffer *bb, int x, int y, char c, uint32_t color, int scale)
{
    int idx = (unsigned char)c;
    if (idx < 0 || idx > 127) idx = 0;
    const uint8_t *glyph = FONT_8X8[idx];

    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                draw_rect(bb, x + col * scale, y + row * scale, scale, scale, color);
            }
        }
    }
}

void draw_text(Backbuffer *bb, int x, int y, const char *text, uint32_t color, int scale)
{
    int cx = x;
    while (*text) {
        draw_char(bb, cx, y, *text, color, scale);
        cx += 8 * scale;
        text++;
    }
}

int text_width(const char *text, int scale)
{
    return (int)strlen(text) * 8 * scale;
}
